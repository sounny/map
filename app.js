/**
 * Compute the bounding box of any GeoJSON object.
 * Returns [[minLng, minLat], [maxLng, maxLat]] or null.
 * @param {object} geojson
 * @returns {[[number,number],[number,number]]|null}
 */
function computeBounds(geojson) {
  let minLng =  Infinity, minLat =  Infinity;
  let maxLng = -Infinity, maxLat = -Infinity;

  function processCoord(coord) {
    if (coord[0] < minLng) minLng = coord[0];
    if (coord[0] > maxLng) maxLng = coord[0];
    if (coord[1] < minLat) minLat = coord[1];
    if (coord[1] > maxLat) maxLat = coord[1];
  }

  function walkGeom(geom) {
    if (!geom) return;
    if (geom.type === 'Point') {
      processCoord(geom.coordinates);
    } else if (geom.type === 'MultiPoint' || geom.type === 'LineString') {
      geom.coordinates.forEach(processCoord);
    } else if (geom.type === 'MultiLineString' || geom.type === 'Polygon') {
      geom.coordinates.forEach(ring => ring.forEach(processCoord));
    } else if (geom.type === 'MultiPolygon') {
      geom.coordinates.forEach(poly => poly.forEach(ring => ring.forEach(processCoord)));
    } else if (geom.type === 'GeometryCollection') {
      geom.geometries.forEach(walkGeom);
    }
  }

  if (geojson.type === 'FeatureCollection') {
    geojson.features.forEach(f => walkGeom(f.geometry));
  } else if (geojson.type === 'Feature') {
    walkGeom(geojson.geometry);
  } else {
    walkGeom(geojson);
  }

  if (!isFinite(minLng)) return null;
  return [[minLng, minLat], [maxLng, maxLat]];
}

/**
 * Escape HTML special characters to prevent XSS in popup content.
 * @param {string} str
 * @returns {string}
 */
function escapeHtml(str) {
  return String(str)
    .replace(/&/g,  '&amp;')
    .replace(/</g,  '&lt;')
    .replace(/>/g,  '&gt;')
    .replace(/"/g,  '&quot;')
    .replace(/'/g,  '&#x27;');
}

/**
 * Display a brief dismissible notification at the bottom of the screen.
 * @param {string} message
 * @param {'info'|'success'|'warning'|'error'} [type='info']
 */
function showToast(message, type = 'info') {
  // Ensure the toast container exists
  let container = document.getElementById('toast-container');
  if (!container) {
    container = document.createElement('div');
    container.id = 'toast-container';
    document.body.appendChild(container);
  }

  const toast = document.createElement('div');
  toast.className = `toast ${type}`;
  toast.textContent = message;
  container.appendChild(toast);

  // Auto-remove after 3.5 s
  setTimeout(() => {
    toast.style.opacity = '0';
    toast.style.transition = 'opacity 0.3s';
    setTimeout(() => toast.remove(), 320);
  }, 3500);
}

