/**
 * Activate Layout Mode:
 *  • Shows the layout overlay elements on the map
 *  • Opens the layout editor panel
 *  • Refreshes dynamic elements (legend, scale)
 */
function activateLayoutMode() {
  layoutModeActive = true;

  document.getElementById('layout-overlay').classList.remove('hidden');
  document.getElementById('layout-panel').classList.remove('hidden');
  document.getElementById('layout-mode-btn').classList.add('active');

  syncLayoutOverlay();
  updateLayoutScale();
  showToast('Layout mode active – configure elements then export.', 'info');
}

/**
 * Deactivate Layout Mode:
 *  • Hides all overlay elements
 *  • Closes the editor panel
 */
function deactivateLayoutMode() {
  layoutModeActive = false;

  document.getElementById('layout-overlay').classList.add('hidden');
  document.getElementById('layout-panel').classList.add('hidden');
  document.getElementById('layout-mode-btn').classList.remove('active');

  // Hide individual elements
  ['lo-title', 'lo-north', 'lo-legend', 'lo-scale', 'lo-sources'].forEach(id => {
    document.getElementById(id).classList.add('hidden');
  });
}

/**
 * Read the current state of all layout checkboxes / inputs and update
 * the overlay elements accordingly.
 */
function syncLayoutOverlay() {
  // Title
  const showTitle = document.getElementById('lp-show-title').checked;
  const titleEl   = document.getElementById('lo-title');
  titleEl.classList.toggle('hidden', !showTitle);
  if (showTitle) {
    document.getElementById('lo-title-text').textContent =
      document.getElementById('lp-title-input').value || 'Map Title';
  }

  // North arrow
  document.getElementById('lo-north')
    .classList.toggle('hidden', !document.getElementById('lp-show-north').checked);

  // Legend
  const showLegend = document.getElementById('lp-show-legend').checked;
  document.getElementById('lo-legend').classList.toggle('hidden', !showLegend);
  if (showLegend) buildLayoutLegend();

  // Scale
  const showScale = document.getElementById('lp-show-scale').checked;
  document.getElementById('lo-scale').classList.toggle('hidden', !showScale);
  if (showScale) updateLayoutScale();

  // Sources
  const showSources  = document.getElementById('lp-show-sources').checked;
  const sourcesEl    = document.getElementById('lo-sources');
  sourcesEl.classList.toggle('hidden', !showSources);
  if (showSources) {
    document.getElementById('lo-sources-text').textContent =
      document.getElementById('lp-sources-input').value || '';
  }
}

/**
 * Populate the legend overlay with entries for every visible layer.
 */
function buildLayoutLegend() {
  const container = document.getElementById('lo-legend-content');
  container.innerHTML = '';

  const visible = Object.values(layers).filter(l => l.visible);
  if (visible.length === 0) {
    container.innerHTML = '<p style="font-size:11px;color:#666">No layers loaded</p>';
    return;
  }

  visible.forEach(info => {
    const item = document.createElement('div');
    item.className = 'lo-legend-item';
    item.innerHTML = `
      <span class="lo-legend-swatch" style="background:${info.color}"></span>
      <span>${escapeHtml(info.name)}</span>
    `;
    container.appendChild(item);
  });
}

