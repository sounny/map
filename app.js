/**
 * Compute the map scale in km for a fixed pixel width (100 px) and
 * update the scale bar element in the layout overlay.
 *
 * Formula: metres per pixel at the current zoom and latitude.
 *   metersPerPx = (earthCircumference * cos(lat)) / (2^zoom * tileSize)
 */
function updateLayoutScale() {
  const SCALE_BAR_PX = 100; // width in pixels to represent on the bar

  const zoom   = map.getZoom();
  const lat    = map.getCenter().lat;
  const metersPerPx =
    (40075016.686 * Math.abs(Math.cos((lat * Math.PI) / 180))) /
    (Math.pow(2, zoom) * 256);

  let totalMetres = metersPerPx * SCALE_BAR_PX;
  let label;

  // Round to a "nice" number for readability
  const nice = niceNumber(totalMetres);
  const niceWidth = Math.round(nice / metersPerPx);

  if (nice >= 1000) {
    label = `${(nice / 1000).toFixed(nice >= 10000 ? 0 : 1)} km`;
  } else {
    label = `${Math.round(nice)} m`;
  }

  document.getElementById('lo-scale-bar').style.width  = `${niceWidth}px`;
  document.getElementById('lo-scale-label').textContent = label;
}

/**
 * Round a number down to the nearest "nice" value (1, 2, 5, 10, 20, 50, …)
 * @param {number} value
 * @returns {number}
 */
function niceNumber(value) {
  const exp  = Math.floor(Math.log10(value));
  const frac = value / Math.pow(10, exp);
  let nice;
  if      (frac < 1.5) nice = 1;
  else if (frac < 3.5) nice = 2;
  else if (frac < 7.5) nice = 5;
  else                 nice = 10;
  return nice * Math.pow(10, exp);
}

