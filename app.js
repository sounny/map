/**
 * Show a popup with the clicked feature's properties.
 * @param {maplibregl.MapMouseEvent} e
 */
function featureClickHandler(e) {
  const feature = e.features[0];
  if (!feature) return;

  const props = feature.properties || {};
  const keys  = Object.keys(props);

  let content = '<div class="popup-title">Feature Properties</div>';

  if (keys.length === 0) {
    content += '<p style="color:var(--text-muted);font-size:12px">No properties.</p>';
  } else {
    content += '<table class="popup-table">';
    keys.forEach(k => {
      const val = props[k];
      // Truncate very long values
      const display = (typeof val === 'string' && val.length > 80)
        ? val.slice(0, 80) + '…'
        : val;
      content += `<tr>
        <td class="prop-key">${escapeHtml(String(k))}</td>
        <td class="prop-val">${escapeHtml(String(display ?? ''))}</td>
      </tr>`;
    });
    content += '</table>';
  }

  new maplibregl.Popup({ closeButton: true, maxWidth: '300px' })
    .setLngLat(e.lngLat)
    .setHTML(content)
    .addTo(map);
}

