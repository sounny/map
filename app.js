/* ============================================================
   4. BASEMAP SWITCHER
   ============================================================ */

/**
 * Switch the active basemap and re-add any user layers on top.
 *
 * When the basemap changes, MapLibre reloads the entire style, which
 * removes all sources and layers that were added programmatically.
 * The 'styledata' event fires once the new style is ready; we then
 * restore every layer stored in the `layers` state object.
 *
 * @param {'street'|'satellite'} basemapKey
 */
function switchBasemap(basemapKey) {
  if (basemapKey === currentBasemap) return;

  currentBasemap = basemapKey;
  const newStyle = basemapKey === 'satellite' ? SATELLITE_STYLE : STREET_STYLE;

  // Style is loaded asynchronously; user layers are restored in the handler below
  map.setStyle(newStyle);

  // Update active state on buttons
  document.querySelectorAll('.basemap-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.basemap === basemapKey);
  });
}

/**
 * After a style change, restore all previously loaded user layers.
 * This fires every time `setStyle` completes.
 */
map.on('styledata', () => {
  Object.values(layers).forEach(layerInfo => {
    if (!map.getSource(layerInfo.sourceId)) {
      addLayerToMap(layerInfo);
    }
  });
});

/* ============================================================
   5. GeoJSON DATA LOADER
   ============================================================ */

/**
 * Read one or more GeoJSON files selected via the file input or dropped
 * onto the upload area, validate them, and add them to the map.
 *
 * @param {FileList|File[]} files
 */
function loadFiles(files) {
  Array.from(files).forEach(file => {
    if (!file.name.match(/\.(geojson|json)$/i)) {
      showToast(`Skipped "${file.name}" – not a GeoJSON file.`, 'warning');
      return;
    }

    const reader = new FileReader();
    reader.onload = (e) => {
      try {
        const geojson = JSON.parse(e.target.result);
        // Validate minimal GeoJSON structure
        if (!geojson.type) throw new Error('Missing "type" property – not valid GeoJSON.');

        const layerName = file.name.replace(/\.(geojson|json)$/i, '');
        loadGeoJSON(layerName, geojson);
        showToast(`Loaded "${layerName}"`, 'success');
      } catch (err) {
        showToast(`Error in "${file.name}": ${err.message}`, 'error');
        console.error('[SounnyMap] GeoJSON parse error:', err);
      }
    };
    reader.readAsText(file);
  });
}

/**
 * Add a GeoJSON object to the map as a new layer.
 * This is the primary entry-point for programmatically adding data –
 * call it from external scripts or agent integrations.
 *
 * @param {string} name   - Human-readable layer name shown in the sidebar
 * @param {object} geojson - Valid GeoJSON FeatureCollection, Feature, or Geometry
 */
function loadGeoJSON(name, geojson) {
  const id     = `layer-${++layerCounter}`;
  const srcId  = `${id}-source`;
  const color  = LAYER_COLORS[(layerCounter - 1) % LAYER_COLORS.length];

  const layerInfo = {
    id,
    sourceId: srcId,
    name,
    color,
    visible: true,
    geojson
  };

  layers[id] = layerInfo;
  addLayerToMap(layerInfo);
  renderLayerList();
  updateLayerCount();

  // Zoom to the loaded data
  const bounds = computeBounds(geojson);
  if (bounds) {
    map.fitBounds(bounds, { padding: 50, maxZoom: 14, duration: 800 });
  }

  // If layout mode is active, refresh the legend
  if (layoutModeActive) buildLayoutLegend();
}

/**
 * Internal: add the MapLibre source + visual layers for a layerInfo object.
 * Handles Points, Lines, and Polygons automatically.
 *
 * To customise the visual style of loaded data (e.g. colour ramps for
 * Earth Observation products), modify the layer paint properties below.
 *
 * @param {object} layerInfo
 */
function addLayerToMap(layerInfo) {
  const { id, sourceId, geojson, color } = layerInfo;

  // Add the GeoJSON as a MapLibre source
  // ── EO / Agent hook ─────────────────────────────────────────────────
  // To display live data, call map.getSource(sourceId).setData(newGeoJSON)
  // when new data arrives from an API or agent tool.
  // ────────────────────────────────────────────────────────────────────
  map.addSource(sourceId, { type: 'geojson', data: geojson });

  // Polygon fill layer
  map.addLayer({
    id: `${id}-fill`,
    type: 'fill',
    source: sourceId,
    filter: ['==', ['geometry-type'], 'Polygon'],
    paint: {
      'fill-color': color,
      'fill-opacity': 0.35
    }
  });

  // Polygon + LineString outline / stroke layer
  map.addLayer({
    id: `${id}-line`,
    type: 'line',
    source: sourceId,
    filter: ['any',
      ['==', ['geometry-type'], 'LineString'],
      ['==', ['geometry-type'], 'Polygon'],
      ['==', ['geometry-type'], 'MultiLineString'],
      ['==', ['geometry-type'], 'MultiPolygon']
    ],
    paint: {
      'line-color': color,
      'line-width': 2
    }
  });

  // Point / circle layer
  map.addLayer({
    id: `${id}-circle`,
    type: 'circle',
    source: sourceId,
    filter: ['==', ['geometry-type'], 'Point'],
    paint: {
      'circle-color': color,
      'circle-radius': 5,
      'circle-stroke-color': '#fff',
      'circle-stroke-width': 1.5
    }
  });

  // Attach click handlers for feature popups
  [`${id}-fill`, `${id}-line`, `${id}-circle`].forEach(lid => {
    map.on('click', lid, featureClickHandler);
    map.on('mouseenter', lid, () => { map.getCanvas().style.cursor = 'pointer'; });
    map.on('mouseleave', lid, () => { map.getCanvas().style.cursor = ''; });
  });
}

/* ============================================================
   6. LAYER MANAGEMENT
   ============================================================ */

/**
 * Toggle a layer's visibility on / off.
 * @param {string} layerId
 */
function toggleLayerVisibility(layerId) {
  const info = layers[layerId];
  if (!info) return;

  info.visible = !info.visible;
  const visibility = info.visible ? 'visible' : 'none';

  [`${layerId}-fill`, `${layerId}-line`, `${layerId}-circle`].forEach(lid => {
    if (map.getLayer(lid)) {
      map.setLayoutProperty(lid, 'visibility', visibility);
    }
  });

  renderLayerList();
  if (layoutModeActive) buildLayoutLegend();
}

/**
 * Remove a layer and its source from the map and from state.
 * @param {string} layerId
 */
function removeLayer(layerId) {
  const info = layers[layerId];
  if (!info) return;

  [`${layerId}-fill`, `${layerId}-line`, `${layerId}-circle`].forEach(lid => {
    if (map.getLayer(lid)) map.removeLayer(lid);
  });
  if (map.getSource(info.sourceId)) map.removeSource(info.sourceId);

  delete layers[layerId];
  renderLayerList();
  updateLayerCount();
  if (layoutModeActive) buildLayoutLegend();
}

/**
 * Zoom the map to fit a specific layer.
 * @param {string} layerId
 */
function zoomToLayer(layerId) {
  const info = layers[layerId];
  if (!info) return;
  const bounds = computeBounds(info.geojson);
  if (bounds) map.fitBounds(bounds, { padding: 60, duration: 700 });
}

/**
 * Update the live data of an existing layer (e.g. from an agent feed).
 * Useful for real-time EO data updates without removing/re-adding the layer.
 *
 * @param {string} layerId  - the ID returned / tracked in the `layers` state
 * @param {object} newGeoJSON - fresh GeoJSON data
 */
function updateLayerData(layerId, newGeoJSON) {
  const info = layers[layerId];
  if (!info) return;
  info.geojson = newGeoJSON;
  const src = map.getSource(info.sourceId);
  if (src) src.setData(newGeoJSON);
}

/** Re-render the layer list in the sidebar */
function renderLayerList() {
  const container = document.getElementById('layer-list');
  const ids = Object.keys(layers);

  if (ids.length === 0) {
    container.innerHTML = '<p class="empty-hint">No layers loaded.<br>Upload a GeoJSON file to begin.</p>';
    return;
  }

  container.innerHTML = '';
  // Show most-recently added first
  [...ids].reverse().forEach(id => {
    const info = layers[id];
    const item = document.createElement('div');
    item.className = 'layer-item';
    item.dataset.layerId = id;

    item.innerHTML = `
      <span class="layer-color" style="background:${info.color}"></span>
      <span class="layer-name${info.visible ? '' : ' dimmed'}" title="${info.name}">${info.name}</span>
      <div class="layer-actions">
        <button class="layer-btn" data-action="zoom"    title="Zoom to layer">⊕</button>
        <button class="layer-btn" data-action="toggle"  title="${info.visible ? 'Hide' : 'Show'} layer">${info.visible ? '👁' : '🚫'}</button>
        <button class="layer-btn danger" data-action="remove" title="Remove layer">✕</button>
      </div>
    `;

    // Event delegation on the item
    item.addEventListener('click', (e) => {
      const btn = e.target.closest('[data-action]');
      if (!btn) return;
      const action = btn.dataset.action;
      if (action === 'zoom')    zoomToLayer(id);
      if (action === 'toggle')  toggleLayerVisibility(id);
      if (action === 'remove')  removeLayer(id);
    });

    container.appendChild(item);
  });
}

/** Update the layer count badge in the sidebar */
function updateLayerCount() {
  document.getElementById('layer-count').textContent = Object.keys(layers).length;
}

