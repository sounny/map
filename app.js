/**
 * Add a raster tile layer (WMS, TMS, or XYZ) to the map.
 * Call this function from your EO data pipeline or agent tool.
 *
 * Examples:
 *   // NASA GIBS MODIS True Colour
 *   addRasterLayer('modis', 'https://gibs.earthdata.nasa.gov/wmts/epsg3857/best/' +
 *     'MODIS_Terra_CorrectedReflectance_TrueColor/default/2024-01-01/GoogleMapsCompatible/{z}/{y}/{x}.jpg');
 *
 *   // Custom tile server
 *   addRasterLayer('eo-product', 'https://my-tile-server.example.com/tiles/{z}/{x}/{y}.png');
 *
 * @param {string} id        - Unique layer ID
 * @param {string} tilesUrl  - XYZ tile URL template
 * @param {object} [opts]    - Optional overrides: { attribution, minzoom, maxzoom, opacity }
 */
function addRasterLayer(id, tilesUrl, opts = {}) {
  const sourceId = `${id}-raster-src`;

  if (map.getSource(sourceId)) {
    console.warn(`[SounnyMap] Source "${sourceId}" already exists. Remove it first.`);
    return;
  }

  map.addSource(sourceId, {
    type: 'raster',
    tiles: [tilesUrl],
    tileSize: opts.tileSize || 256,
    attribution: opts.attribution || ''
  });

  map.addLayer({
    id: `${id}-raster-layer`,
    type: 'raster',
    source: sourceId,
    minzoom: opts.minzoom ?? 0,
    maxzoom: opts.maxzoom ?? 22,
    paint: {
      'raster-opacity': opts.opacity ?? 0.85
    }
  });
}

